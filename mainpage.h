/**
 * @mainpage Система построения иерархий классов на основе правил вывода
 *
 * @section intro Введение
 * Данная программа предназначена для построения иерархий классов на основе
 * заданных правил вывода. Программа читает входной JSON-файл с классами
 * и их правилами вывода, строит иерархию связей "тип-подтип" и сохраняет
 * результат в DOT-файл для визуализации.
 *
 * @section features Функциональные возможности
 * - Поддержка 4 типов правил вывода для свойств:
 *   - **Тип 1**: наличие свойства с заданным именем
 *   - **Тип 2**: наличие свойства с заданным количеством значений
 *   - **Тип 3**: наличие свойства с заданным значением
 *   - **Тип 4**: наличие свойства с заданным массивом значений
 * - Автоматическое построение связей "родитель-потомок" на основе общих свойств
 * - Удаление транзитивных связей для оптимизации иерархии
 * - Валидация входных данных с информативными сообщениями об ошибках
 * - Генерация DOT-файла для визуализации иерархии классов
 *
 * @section usage Использование
 * Запуск программы осуществляется из командной строки:
 * @code{.cmd}
 * build_class_hierarchy.exe <входной_файл.json> <выходной_файл.dot>
 * @endcode
 *
 * **Пример:**
 * @code{.cmd}
 * build_class_hierarchy.exe C:\\Documents\\classes.json .\\class_hierarchy.dot
 * @endcode
 *
 * @section input_format Входной формат (JSON)
 * Входной файл содержит JSON-объект с обязательным полем "classes":
 * @code{.json}
 * {
 *     "classes": [
 *         {
 *             "class_name": "BaseDevice",
 *             "properties": [
 *                 {
 *                     "name": "power_source"
 *                 }
 *             ]
 *         },
 *         {
 *             "class_name": "SmartDevice",
 *             "properties": [
 *                 {
 *                     "name": "power_source"
 *                 },
 *                 {
 *                     "name": "wifi_module"
 *                 }
 *             ]
 *         },
 *         {
 *             "class_name": "IoT_Sensor",
 *             "properties": [
 *                 {
 *                     "name": "power_source"
 *                 },
 *                 {
 *                     "name": "wifi_module"
 *                 },
 *                 {
 *                     "name": "sensors",
 *                     "value_count": 3
 *                 }
 *             ]
 *         }
 *     ]
 * }
 * @endcode
 *
 * @section output_format Выходной формат (DOT)
 * Выходной файл содержит иерархию классов на языке DOT:
 * @code{.dot}
 * digraph G {
 *     node [shape=oval, fontname="Arial"];
 *     "BaseDevice" -> "SmartDevice";
 *     "SmartDevice" -> "IoT_Sensor";
 *     "BaseDevice" -> "LegacyTerminal";
 * }
 * @endcode
 *
 * @section classes Основные сущности
 * - @ref ClassNode — класс с именем и набором свойств
 * - @ref PropertyRule — правило для свойства (имя + тип правила)
 * - @ref ClassHierarchy — иерархия классов с связями
 * - @ref Error — структура для хранения информации об ошибках
 * - @ref PropertyRuleType — перечисление типов правил вывода
 *
 * @section property_rules Типы правил вывода
 * - **Тип 1 (HasProperty)** — класс определяется наличием свойства с указанным именем
 * - **Тип 2 (HasPropertyWithCount)** — класс определяется наличием свойства с указанным именем и количеством значений
 * - **Тип 3 (HasPropertyWithValue)** — класс определяется наличием свойства с указанным именем и значением
 * - **Тип 4 (HasPropertyWithValues)** — класс определяется наличием свойства с указанным именем и массивом значений
 *
 * @section validation Валидация данных
 * Программа проверяет следующие ограничения:
 * - Количество классов: [1, 100]
 * - Количество свойств у класса: [1, 100]
 * - Длина имени класса: [1, 255] символов
 * - Длина имени свойства: [1, 255] символов
 * - Размер массива значений свойства: [1, 100]
 * - Значения свойств: [1, 1000]
 * - Допустимые символы: буквы (рус/англ), цифры, '_', '-'
 * - Количество строк во входном файле: ≤ 100
 *
 * @section error_handling Обработка ошибок
 * При обнаружении ошибки программа выводит сообщение в консоль
 * и завершает работу без создания выходного файла. Поддерживается
 * более 20 типов ошибок с детальным описанием причины.
 *
 * @section algorithms Алгоритмы
 * 1. **Построение иерархии** — для каждой пары классов проверяется,
 *    являются ли свойства одного класса подмножеством свойств другого
 * 2. **Удаление транзитивных связей** — оптимизация иерархии путем
 *    удаления связей, которые можно вывести через промежуточные классы
 * 3. **Генерация DOT** — создание текстового представления графа
 *    на языке DOT для визуализации в Graphviz
 *
 * @section developers Разработчикам
 * Программа разработана на языке C++ с использованием библиотеки Qt.
 * Дополнительного программного обеспечения не требуется.
 */
